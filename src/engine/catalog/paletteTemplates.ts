export function getPaletteTemplate(templateId: string | undefined): PaletteTemplate | undefined {
  if (!templateId) return undefined
  return PALETTE_TEMPLATES[templateId]
}

export function instantiateTemplate(templateId: string): CanvasNodeDataV2 {
  const template = getPaletteTemplate(templateId)
  if (!template) {
    throw new Error(`Unknown palette template: ${templateId}`)
  }
  return createCanvasData(template)
}
