import { getComponentSpec } from './componentSpecs'
import type { CanvasNodeDataV2, PaletteTemplate, SourceConfig } from './nodeSpecTypes'

const DEFAULT_REQUEST_TYPE = 'default'
const DEFAULT_REQUEST_SIZE_BYTES = 1024

function createSourceConfig(seed?: PaletteTemplate['seed']): SourceConfig {
  const pattern = seed?.pattern ?? 'poisson'
  const baseRps = seed?.baseRps ?? seed?.throughput ?? 100

  return {
    requestDistribution: [
      {
        type: seed?.requestType ?? DEFAULT_REQUEST_TYPE,
        weight: 1,
        sizeBytes: seed?.requestSizeBytes ?? DEFAULT_REQUEST_SIZE_BYTES
      }
    ],
    defaultWorkload: {
      pattern,
      baseRps,
      bursty: { burstRps: 500, burstDuration: 2000, normalDuration: 8000 },
      spike: { spikeTime: 30_000, spikeRps: 1000, spikeDuration: 5000 },
      sawtooth: { peakRps: 300, rampDuration: 10_000 }
    }
  }
}

function createCanvasData(template: PaletteTemplate): CanvasNodeDataV2 {
  const spec = getComponentSpec(template.componentType)

  return {
    schemaVersion: 2,
    templateId: template.id,
    componentType: template.componentType,
    structuralRole: template.structuralRole,
    profile: template.profile,
    rendererType: template.rendererType,
    label: template.label,
    subLabel: template.subLabel,
    iconKey: template.iconKey,
    routingStrategy: template.routingStrategy,
    sim:
      template.serializable && spec && template.profile !== 'source'
        ? spec.createDefaultSimulationConfig(template.seed)
        : undefined,
    source: template.profile === 'source' ? createSourceConfig(template.seed) : undefined,
    ui: template.seed?.overloadPreview ? { overloadPreview: true } : undefined
  }
}

